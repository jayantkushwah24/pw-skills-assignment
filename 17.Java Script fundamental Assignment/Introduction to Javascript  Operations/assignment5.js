// Q5. Write a JavaScript program that calculates the simple interest using the formula Simple interest =
// (principal * rate * time) / 100.

let principalAmount = 5000;
let interestRate = 10;
let timePeriod = 2;
let interest = (principalAmount * interestRate * timePeriod)/100;

console.log("Principal Amount:", principalAmount);
console.log("Interest Rate:", interestRate, "%");
console.log("Time Period:", timePeriod, "years");
console.log("Simple Interest:", interest);