// JavaScript function to generate a personalized greeting
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to the world of JavaScript programming.`;
  }
  
  // Greet three different people using the function
  const greeting1 = generateGreeting("Alice");
  const greeting2 = generateGreeting("Bob");
  const greeting3 = generateGreeting("Charlie");
  
  // Display the greetings
  console.log(greeting1);
  console.log(greeting2);
  console.log(greeting3);
  